package example;

public class example {

	public static void main(String[] args) {
		num(9, 3);
	}

	public static void num(int num1, int num2) {
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
	}
}
